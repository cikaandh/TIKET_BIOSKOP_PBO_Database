    private void selectData() {
        String kolom[] = {"NomorIdentitas","NamaPemesan","JudulFilm","JumlahTiket"};
        DefaultTableModel dtm = new DefaultTableModel(null, kolom);
        String SQL = "SELECT * FROM tb_data";
        ResultSet rs = KoneksiDB.executeQuery(SQL);
        try {
            while (rs.next()) {
                String NomorIdentitas = rs.getString(1);
                String NamaPemesan = rs.getString(2);
                String JudulFilm = "";
                if ("Dear Nathan".equals(rs.getString(3))) {
                    JudulFilm = "Dear Nathan";
                } else if ("Harry Potter".equals(rs.getString(3))) {
                    JudulFilm = "Harry Potter";
                }else if ("Lights Out".equals(rs.getString(3))) {
                    JudulFilm = "Lights Out"; 
                }else {
                    JudulFilm = "Van Oranje";
                }
                String JumlahTiket = rs.getString(4);
                String data[] = {NomorIdentitas,NamaPemesan,JudulFilm,JumlahTiket};
                dtm.addRow(data);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Main_Frame.class.getName()).log(Level.SEVERE, null, ex);
        }
        tb1Data.setModel(dtm);
    }
