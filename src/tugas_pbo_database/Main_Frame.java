    private void btnPESANActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPESANActionPerformed
        if ("".equals(txtNOMOR.getText()) || "".equals(txtNAMA.getText()) || "".equals(txtJUMLAH.getText()) ) {
            JOptionPane.showMessageDialog(this, "Harap Lengkapi Data", "Error", JOptionPane.WARNING_MESSAGE);
        } else{
            String JF = "";
            if (rdFILM1.isSelected()) {
                JF = "Dear Nathan";
            }else if (rdFILM2.isSelected()) {
                JF = "Harry Potter";
            }else if (rdFILM3.isSelected()) {
                JF = "Lights Out";
            }else{
                JF = "Van Oranje";
            }
            String SQL = "INSERT INTO tb_data (NomorIdentitas,NamaPemesan,JudulFilm,JumlahTiket)"
                    + "VALUES('"+txtNOMOR.getText()+"','"+txtNAMA.getText()+"','"+JF+"','"+txtJUMLAH.getText()+"')";
            int status = KoneksiDB.execute(SQL);
            if (status == 1) {
                JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan", "Sukses", JOptionPane.INFORMATION_MESSAGE);
                selectData();
            } else {
                JOptionPane.showMessageDialog(this, "Data Gagal Ditambahkan", "Sukses", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnPESANActionPerformed

